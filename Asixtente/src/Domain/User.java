package Domain;

/**
 *
 * @author Danny Logax
 */
public class User {
    String user;
    String password;

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public String toString() {
        return "User{" + "user=" + user + ", password=" + password + '}';
    }
    
}
